package main

import "fmt"

func main(){
    fmt.Println("Hola" + "Mundo")
    fmt.Println("Hola", "Mundo", "!!!")
    fmt.Println("5+5 =", 5+5)
    fmt.Println("7.5/3.2=", 7.5/3.2)
    fmt.Println("7/3=", 7/3)
    fmt.Println("7/3=", 7.0/3.0)
    fmt.Println(true && false)
    fmt.Println(true || false)
    fmt.Println(!true)
}
